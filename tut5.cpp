#include<iostream>
// There are two type of header files
using namespace std;

int main(){
    
    int a = 5, b = 4;
    cout<<"This is hello world"<<endl;
    cout<<"following are the types of operators in c++"<<endl;
    //Arithmetic operators
    cout<<"The value of a + b is "<<a+b<<endl;
    cout<<"The value of a - b is "<<a-b<<endl;
    cout<<"The value of a * b is "<<a*b<<endl;
    cout<<"The value of a / b is "<<a/b<<endl;
    cout<<"The value of a % b is "<<a%b<<endl;
    cout<<"The value of a ++ is "<<a++<<endl;
    cout<<"The value of a -- is "<<a--<<endl;
    cout<<"The value of ++ a is "<<++a<<endl;
    cout<<"The value of -- a is "<<--a<<endl;
    return 0;

}